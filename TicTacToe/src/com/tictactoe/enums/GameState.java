package com.tictactoe.enums;

/**
 * Represents the possible states of a game.
 */
public enum GameState {
    /**
     * The game is currently in progress.
     */
    IN_PROGRESS,

    /**
     * The game has ended with a winner.
     */
    ENDED,

    /**
     * The game has ended in a draw.
     */
    DRAW
}
